public class Cuadrado {

    private int valor;
    public Cuadrado(int valor) {
        this.valor = valor;
    }

    public int getValor() {
        return valor;
    }
    public int getCuadrado() {
        return valor*valor;
    }
    public void setValor(int valor) {
        this.valor = valor;
    }

    
    
    
    
}
